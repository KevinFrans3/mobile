
public class Branch {
	
	public int length;
	
	public boolean isWeight = false;
	public int weight;
	public Mobile mobile;
	
	public Branch()
	{
		
	}
	
	public int getLength()
	{
		return length;
	}
	
	public boolean getIsWeight()
	{
		return isWeight;
	}
	
	public int getWeight()
	{
		return weight;
	}
	
	public Mobile getMobile()
	{
		return mobile;
	}
	
	

}
